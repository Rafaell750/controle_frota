class Veiculo:
    def __init__(self, marca, modelo, placa, tipo, capacidade):
        self.marca = marca
        self.modelo = modelo
        self.placa = placa
        self.tipo = tipo
        self.capacidade = capacidade


def __str__(self):
        return f"{self.modelo} ({self.marca}) - Placa: {self.placa}, Tipo: {self.tipo}, Capacidade: {self.capacidade} pessoas"